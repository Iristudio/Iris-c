#include<iostream>
using namespace std;

int main() {
	int a = 10;
	int b = 20;

	// == 等于(注意 在c++中 =是赋值 ==才是相等)
	cout << (a == b) << endl;

	// != 不等于
	cout << (a != b) << endl;

	// < 小于
	cout << (a < b) << endl;

	// > 大于
	cout << (a > b) << endl;

	// <= 小于等于
	cout << (a <= b) << endl;

	// >=大于等于
	cout << (a >= b) << endl;



	system("pause");
	return 0;
}